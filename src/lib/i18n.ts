export type TranslationKey = keyof typeof translations.en

const languageStorageKey = 'luma-heic-language'
const legacyLanguageStorageKey = 'hdr-heic-bypass-language'

export function getInitialLanguage(): Language {
  const saved =
    window.localStorage.getItem(languageStorageKey) ??
    window.localStorage.getItem(legacyLanguageStorageKey)
  if (saved === 'en' || saved === 'zh') return saved
  return navigator.language.toLowerCase().startsWith('zh') ? 'zh' : 'en'
}

export function saveLanguage(language: Language) {
  window.localStorage.setItem(languageStorageKey, language)
  window.localStorage.removeItem(legacyLanguageStorageKey)
}
